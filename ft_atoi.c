#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	i;
	int				res;
	int				m;

	i = 0;
	m = 1;
	res = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		m *= -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = (res * 10) + (str[i] - 48);
		i++;
	}
	res = res * m;
	return (res);
}
